#include<stdio.h>



int main() {
    printf("这是数字一 ：%d\n", 1);
    int a = 1;
    printf("这是数字一 ：%d\n", a);
    
    char b = 'A';
    printf("这是字符A : %c\n", 'A');
    printf("这是字符A : %c\n", b);

    float c = 2.1;
    printf("这是浮点数: %f\n", 2.1);
    printf("这是浮点数: %f\n", c);

    long long d = 100;
    printf("这是长整型：%lld\n", 100);
    printf("这是长整型：%lld\n", d);
    return 0;
}

